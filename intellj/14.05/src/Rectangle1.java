public class Rectangle1
{
	// instance variables 
	private int length;
	private int width;

	/**
	 * Constructor for objects of class rectangle
	 */
	public Rectangle1(int l, int w)
	{
		// initialise instance variables
		length = l;
		width = w;
	}
	// return the height
	public int getLength()
	{
		return length;
	}
	public int getWidth()
	{
	    return width;
	}
	   
}
